-- Rebuild category paths for all categories
DO $$
DECLARE
    cat RECORD;
BEGIN
    -- First, clear all paths
    DELETE FROM category_paths;

    -- Insert self-references
    INSERT INTO category_paths (ancestor_id, descendant_id, depth)
    SELECT id, id, 0 FROM categories;

    -- Build paths using recursive approach
    WITH RECURSIVE category_tree AS (
        -- Base: root categories
        SELECT id, id as root_id, 0 as depth
        FROM categories
        WHERE parent_id IS NULL

        UNION ALL

        -- Recursive: children
        SELECT c.id, ct.root_id, ct.depth + 1
        FROM categories c
        JOIN category_tree ct ON c.parent_id = ct.id
    )
    INSERT INTO category_paths (ancestor_id, descendant_id, depth)
    SELECT
        p.id as ancestor_id,
        c.id as descendant_id,
        c.depth - p.depth as depth
    FROM category_tree c
    JOIN category_tree p ON c.root_id = p.root_id AND p.depth < c.depth
    ON CONFLICT DO NOTHING;
END $$;

